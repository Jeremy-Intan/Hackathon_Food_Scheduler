def randNew(day, hour):
    outHour = []
    
    for i in range(0, len(prob[day][hour])):
        outHour.append(0)
        
    x = randint(1, 100)
    for i in range(0, len(prob[day][hour])):
        if x <= prob[day][hour][i]:
            outHour[i] = 1
            break
        else:
            x = x - prob[day][hour][i]

    return outHour

